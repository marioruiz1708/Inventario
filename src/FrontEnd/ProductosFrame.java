    private void ButtomImprimirBarrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtomImprimirBarrasActionPerformed
        if(lblcode.getIcon() == null){
            JOptionPane.showMessageDialog(null, "No ha Visualizado el codigo");
        }else{
          Barcode barcode = null;
         try {
            barcode = BarcodeFactory.createCode128(txtBarrasAgregar.getText());
        } catch (Exception e) {
        }
          barcode.setDrawingText(true);
         
            barcode.setBarWidth(2);
            barcode.setBarHeight(60);
        
            PrinterJob job = PrinterJob.getPrinterJob();
                 job.setPrintable(barcode);
                 
                 if (job.printDialog()){
                         try {
                              job.print();
                         } catch (PrinterException ex) {
                             Logger.getLogger(ProductosFrame.class.getName()).log(Level.SEVERE, null, ex);
                             }
                   }
        }
    }//GEN-LAST:event_ButtomImprimirBarrasActionPerformed

    private void ButtomVisualizarBarrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtomVisualizarBarrasActionPerformed
         if(txtBarrasAgregar.getText().isEmpty()){
             JOptionPane.showMessageDialog(null, "No ha escrito el codigo");
        }else{
         Barcode barcode = null;
         try {
            barcode = BarcodeFactory.createCode128(txtBarrasAgregar.getText());
        } catch (Exception e) {
        }
         try{
          barcode.setDrawingText(true);
         
            barcode.setBarWidth(1);
            barcode.setBarHeight(50);
            BufferedImage image = new BufferedImage(300, 100, BufferedImage.TYPE_INT_ARGB);
            Graphics2D g = (Graphics2D) image.getGraphics();
             
            barcode.draw(g, 5, 20);
        
         ImageIcon icon = new ImageIcon(image);
           lblcode.setIcon(icon);
           }catch (Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
        }
    }//GEN-LAST:event_ButtomVisualizarBarrasActionPerformed

